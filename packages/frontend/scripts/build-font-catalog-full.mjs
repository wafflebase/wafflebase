// @ts-nocheck
/**
 * Generates `src/components/text-formatting/font-catalog.full.ts` — the
 * full Google Fonts library (~1,900 families) browsed in the "More
 * fonts…" dialog. Dynamic-imported on demand, so it never ships in the
 * main editor bundle.
 *
 * Run on demand (NOT part of the build), then commit the output:
 *
 *   node packages/frontend/scripts/build-font-catalog-full.mjs
 *
 * Sources:
 *   - fontsource `google-font-metadata` (one request) — family name,
 *     category, subsets, and the exact weight array per family. The
 *     weight array lets us pick a safe `:wght@…` spec without poisoning
 *     the per-family <link>.
 *   - the `google/fonts` repo git-trees (four requests) — the license
 *     directory (ofl / apache / ufl) each family lives under, since the
 *     webfonts REST API and the fontsource data both omit license.
 *     Falls back to OFL (the ~99% case) if the tree fetch fails.
 */
import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const OUT = join(
  dirname(fileURLToPath(import.meta.url)),
  "../src/components/text-formatting/font-catalog.full.ts",
);

const METADATA_URL =
  "https://unpkg.com/google-font-metadata/data/google-fonts-v2.json";
const GH_TREES = "https://api.github.com/repos/google/fonts/git/trees";
const LICENSE_OF = { ofl: "OFL", apache: "APACHE2", ufl: "UFL" };

const CATEGORY_TO_GROUP = {
  "sans-serif": "Sans-serif",
  serif: "Serif",
  monospace: "Monospace",
  display: "Display",
  handwriting: "Handwriting",
};

/** css2 `wght@…` spec from the exact weight array: prefer 400;700, else
 *  the lightest available so single-cut faces don't request a missing
 *  weight. */
function weightsSpec(weights) {
  const set = new Set(weights);
  const wanted = [400, 700].filter((w) => set.has(w));
  if (wanted.length > 0) return wanted.join(";");
  if (weights.length > 0) return String([...weights].sort((a, b) => a - b)[0]);
  return "400";
}

/** Map google/fonts dir slug → license, via the git-trees API. One root
 *  request + one per license dir. Returns a Map; empty on any failure. */
async function fetchLicenseMap() {
  const map = new Map();
  try {
    const root = await (await fetch(`${GH_TREES}/main`)).json();
    for (const node of root.tree ?? []) {
      const license = LICENSE_OF[node.path];
      if (!license || node.type !== "tree") continue;
      const sub = await (await fetch(`${GH_TREES}/${node.sha}`)).json();
      for (const fam of sub.tree ?? []) {
        if (fam.type === "tree") map.set(fam.path, license);
      }
    }
  } catch (err) {
    console.warn(`! license map fetch failed, defaulting to OFL: ${err.message}`);
  }
  return map;
}

async function build() {
  const data = await (await fetch(METADATA_URL)).json();
  const licenseMap = await fetchLicenseMap();

  const entries = [];
  for (const id of Object.keys(data)) {
    const f = data[id];
    const subsets = (f.subsets ?? []).filter((s) => s !== "menu");
    const group = subsets.includes("korean")
      ? "Korean"
      : (CATEGORY_TO_GROUP[f.category] ?? "Sans-serif");
    // fontsource id ("noto-sans-kr") → google/fonts dir ("notosanskr").
    const ghSlug = id.replace(/-/g, "");
    entries.push({
      label: f.family,
      family: f.family,
      group,
      webFont: true,
      weights: weightsSpec(f.weights ?? [400]),
      license: licenseMap.get(ghSlug) ?? "OFL",
      scripts: subsets,
    });
  }
  entries.sort((a, b) => a.family.localeCompare(b.family));

  await writeFile(OUT, emit(entries), "utf8");
  console.log(
    `wrote ${entries.length} families → ${OUT} ` +
      `(${licenseMap.size} licenses resolved from google/fonts)`,
  );
}

function emit(entries) {
  const lines = entries.map((e) => {
    const parts = [
      `label: ${JSON.stringify(e.label)}`,
      `family: ${JSON.stringify(e.family)}`,
      `group: ${JSON.stringify(e.group)}`,
      `webFont: true`,
      `weights: ${JSON.stringify(e.weights)}`,
      `license: ${JSON.stringify(e.license)}`,
      `scripts: ${JSON.stringify(e.scripts)}`,
    ];
    return `  { ${parts.join(", ")} },`;
  });
  return (
    `// GENERATED by scripts/build-font-catalog-full.mjs — do not edit by hand.\n` +
    `// The full Google Fonts library for the "More fonts…" dialog;\n` +
    `// dynamic-imported so it stays out of the main editor bundle.\n` +
    `import type { FontEntry } from "./font-catalog";\n\n` +
    `export const FONT_CATALOG_FULL: readonly FontEntry[] = [\n` +
    `${lines.join("\n")}\n];\n`
  );
}

build().catch((err) => {
  console.error(err);
  process.exit(1);
});
